/**
 * op_add - returns the sum of a and b
 * @a: integer to be added
 * @b: integer to be added
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference of a and b
 * @a: inteer to be subtracted
 * @b: integer to be subtracted
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - returns the result of division between a and b
 * @a: integer to be divided
 * @b: integer to be divided
 * Return: result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - returns the result of multiplication between a and b
 * @a: integer to be multiplied
 * @b: integer to be multiplied
 * Return: result of multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - returns the remainder of the division between a and b
 * @a: first integer
 * @b: second integer
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
